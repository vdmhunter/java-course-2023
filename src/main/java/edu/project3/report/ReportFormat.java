package edu.project3.report;

/**
 * This enumeration defines the available formats for the report.
 * The formats supported are Markdown and AsciiDoc.
 */
public enum ReportFormat {
    /**
     * This format represents a report in Markdown format.
     */
    MARKDOWN,
    /**
     * This format represents a report in AsciiDoc format.
     */
    ADOC
}
